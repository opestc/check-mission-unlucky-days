from functools import reduce
from operator import or_
from itertools import starmap
from typing import List

points={(-1,0):'N',(1,0):'S',(0,1):'E',(0,-1):'W'}

def neighbours(c, r):
    return {(c+1, r), (c-1, r), (c, r+1), (c,r-1)}

def expand(positions):
    return reduce(or_, starmap(neighbours, positions), positions) 

def direction(pos1,pos2):
    if (pos1[0]-pos2[0],pos1[1]-pos2[1]) in points.keys():
        return points[(pos1[0]-pos2[0],pos1[1]-pos2[1])]
    return ''
        
def directions(pos,*args):
    return [(arg,direction(pos,arg)) for arg in args if direction(pos, arg)]

def checkio(maze_map):
    start=(1,1)
    end=(10,10)
    visited = {(r,c) for r in range(len(maze_map)) for c in range(len(maze_map[0])) if maze_map[r][c]==1} | {start}
    rountes={}
    rountes[start]=['']
    to_visit = expand({start}) - visited
    pre_visited=[start]
    steps = 0 
    while to_visit:
        steps += 1
        print('to visit',to_visit)
        for pos in to_visit:
            rountes[pos]=[]
            for point in directions(pos, *pre_visited):
                print('point',point)
                rountes[pos]+=[rounte+point[1] for rounte in rountes[point[0]]]
        #print('rountes',rountes)
        visited |= to_visit
        pre_visited=list(to_visit)
        to_visit = expand(to_visit) - visited
        if visited & {end}:
            #print(rountes[end])
            return rountes[end][0]

